package tema1;

public class Ejercicio1 {
    public static void main(String[] args) {

        // 1. Numericos


        // 1.1 Enteros

        byte variable1 = 5;
        short variable2 = 10;
        int variable3 = 30;
        long variable4 = 100;

        System.out.println("Los números: " + variable1 + ", " + variable2 + ", " + variable3 + " y " + variable4 + " son números enteros.");

        // 1.2 Decimales

        float variable5 = 5.5f;
        double variable6 = 10.5d;

        System.out.println("Los números : " + variable5 + " y " + variable6 + " son números decimales.");

        // 2. Booleano

        boolean variable7 = false;
        boolean variable8 = true;

        System.out.println("Las variables: " + variable7 + " y " + variable8 + " son booleanas.");
        // 3. Texto

        char variable9 = 'a';
        String variable10 = "Lorem ipsum dolor sit amet...";
        System.out.println("Estas son texto: " + variable9 + " / " + variable10);
    }
}

